    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //neu checkbox duoc chon thi dang nhap duoi quyen admin
        if(checkbox.isSelected()){
            //su ly neu ng dung bo trong
            if(txtUser.getText().equals("")|| txtPass.getText().equals("")){
                JOptionPane.showMessageDialog(this, "Không được để trống");
            }
            else{
                try {
                ConnectMySQL con = new ConnectMySQL();
                //lấy mat khau tai khoan tu database so sanh voi nhap vao
                String check1 = "SELECT * FROM admin where adminTK=? and adminMK=?";
                PreparedStatement pst= con.conn.prepareCall(check1);
                pst.setString(1, txtUser.getText());
                pst.setString(2, txtPass.getText());    
                ResultSet rs = pst.executeQuery();
                if(rs.next()){
                     JOptionPane.showMessageDialog(this, "Đăng nhập thành công");
                    Main main = new Main();
                    main.setVisible(true);
                    this.setVisible(false);    
                }
                else
                    JOptionPane.showMessageDialog(this, "Sai tài khoản hoặc mật khẩu");
            } catch (Exception e) {
                
            }
            }  
        } else{//neu checkbõx khong duoc chon dang nhap vao khach
            try {
                if(txtUser.getText().equals("")|| txtPass.getText().equals("")){
                JOptionPane.showMessageDialog(this, "Không được để trống");
            }
            else{
                try {
                ConnectMySQL con = new ConnectMySQL();
                //lấy mat khau tai khoan tu database so sanh voi nhap vao
                String check1 = "SELECT * FROM dangnhap where TK=? and MK=?";
                PreparedStatement pst= con.conn.prepareCall(check1);
                pst.setString(1, txtUser.getText());
                pst.setString(2, txtPass.getText());    
                ResultSet rs = pst.executeQuery();
                if(rs.next()){
                     JOptionPane.showMessageDialog(this, "Đăng nhập thành công");
                    XH_MH_MainForm xh = new XH_MH_MainForm();
                    xh.setVisible(true);
                    this.setVisible(false);    
                }
                else
                    JOptionPane.showMessageDialog(this, "Sai tài khoản hoặc mật khẩu");
            } catch (Exception e) {
                e.printStackTrace();
            }
            }  
            } catch (Exception e) {
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed
 
