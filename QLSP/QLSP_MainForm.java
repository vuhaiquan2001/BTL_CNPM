    //load bang
    private void loadbang(){
        tbQLSP.removeAll();
        try {
            //kết nối với MySQL
            cnpm_btl.ConnectMySQL con = new cnpm_btl.ConnectMySQL();
            ResultSet rs = con.getdata("sanpham");
            int i = 0;
            while(rs.next()){        
                tbQLSP.setValueAt(rs.getString("MaSP"), i, 0);
                tbQLSP.setValueAt(rs.getString("TenSP"), i, 1);
                tbQLSP.setValueAt(rs.getString("LoaiSP"), i, 2);
                tbQLSP.setValueAt(rs.getString("XuatSu"), i, 3);
                tbQLSP.setValueAt(rs.getInt("GiaSP"), i, 4);
                tbQLSP.setValueAt(rs.getInt("SL"), i, 5);
                i++;
            }
            
        } catch (Exception e) {
        }
    }
