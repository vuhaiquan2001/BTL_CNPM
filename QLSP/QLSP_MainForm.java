    private void xoaTextField(){
        txtMaSP.setText("");
        txtTenSP.setText("");
        txtLoaiSP.setText("");
        txtGia.setText("");
        txtXuatSu.setText("");
        txtSL.setText("");
        loadbang();
        txtMaSP.setEnabled(true);
    }
           
